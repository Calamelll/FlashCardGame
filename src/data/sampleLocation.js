// Helper function to get a random location
export const getRandomLocation = () => {
  const randomIndex = Math.floor(Math.random() * sampleLocations.length);
  return sampleLocations[randomIndex];
};

// Helper function to shuffle locations
export const shuffleLocations = (locations) => {
  return [...locations].sort(() => Math.random() - 0.5);
};
